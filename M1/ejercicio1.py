input_celcius = float(input("Bienvenido, por favor ingrese el valor de grados celcius a convertir a Fahrenheit \n"))
convert_fahrenheit = input_celcius * 1.8 + 32
print("el resultado de " + str(input_celcius) +"ºC "+ "Convertido en Fahrenheit es de: " + str(convert_fahrenheit) + "ºF")
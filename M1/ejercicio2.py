print("\nVamos a realizar un promedio de 3 numeros, los cuales pueden ser numeros /R/, por favor siga las instrucciones.")
num_a = float(input("Por favor ingresa el PRIMER numero \n"))
num_b = float(input("Por favor ingresa el SEGUNDO numero \n"))
num_c = float(input("Por favor ingresa el TERCER numero \n"))
promedio =  str((num_a + num_b + num_c) / 3)
print("el promedio de la suma de los numeros: " + str(num_a) + ", " + str(num_b) + ", " + str(num_c) + ", " + " da como resultado: " + promedio)